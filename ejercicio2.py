# Definir los valores de los lados del rectángulo
a = 4  # longitud del lado a en metros
b = 6  # longitud del lado b en metros

# Calcular el perímetro del rectángulo
perimetro = 2 * (a + b)

# Calcular el área del rectángulo
area = a * b

# Mostrar los resultados
print(f"El perímetro del rectángulo es: {perimetro} metros")
print(f"El área del rectángulo es: {area} metros cuadrados")
